export function getHamyarEntity(entityType) {
  return HAMYAR_CAPABILITY_REGISTRY.entities[entityType] || null;
}

export function getHamyarField(entityType, fieldName) {
  return getHamyarEntity(entityType)?.fields?.[fieldName] || null;
}

export function getHamyarIntent(intentId) {
  return HAMYAR_CAPABILITY_REGISTRY.intents[intentId] || null;
}

export function listHamyarIntents() {
  return Object.entries(HAMYAR_CAPABILITY_REGISTRY.intents).map(([id, definition]) => ({ id, ...definition }));
}

export function listHamyarEvalExamples() {
  return listHamyarIntents().flatMap(({ id, examples = [], ...definition }) =>
    examples.map((question) => ({
      question,
      intent: id,
      primaryEntity: definition.primaryEntity,
      relationPath: definition.relationPath,
      requestedField: definition.requestedField,
      preferredEntityTypes: definition.preferredEntityTypes,
      needsCompanyBrain: definition.needsCompanyBrain,
      needsLiveVerification: definition.needsLiveVerification,
      liveTool: definition.liveTool,
      fallback: definition.fallback,
    }))
  );
}

export function validateHamyarCapabilityRegistry(registry = HAMYAR_CAPABILITY_REGISTRY) {
  const errors = [];
  for (const [intentId, definition] of Object.entries(registry.intents || {})) {
    if (!definition.primaryEntity) errors.push(`${intentId}: missing primaryEntity`);
    if (!Array.isArray(definition.relationPath)) errors.push(`${intentId}: relationPath must be an array`);
    if (!definition.requestedField) errors.push(`${intentId}: missing requestedField`);
    if (!Array.isArray(definition.examples) || !definition.examples.length) errors.push(`${intentId}: missing eval examples`);
    if (definition.liveTool && typeof definition.liveTool !== "string") errors.push(`${intentId}: liveTool must be a string`);
  }
  for (const [entityType, definition] of Object.entries(registry.entities || {})) {
    if (!Array.isArray(definition.aliases) || !definition.aliases.length) errors.push(`${entityType}: missing aliases`);
    if (!definition.fields || !Object.keys(definition.fields).length) errors.push(`${entityType}: missing fields`);
  }
  return { ok: errors.length === 0, errors };
}

export function registryToEvalCases(registry = HAMYAR_CAPABILITY_REGISTRY) {
  const validation = validateHamyarCapabilityRegistry(registry);
  if (!validation.ok) {
    const error = new Error(`Invalid Hamyar capability registry: ${validation.errors.join("; ")}`);
    error.validationErrors = validation.errors;
    throw error;
  }
  return listHamyarEvalExamples().map((example, index) => ({
    id: `hamyar-capability-${String(index + 1).padStart(3, "0")}`,
    ...example,
  }));
}
