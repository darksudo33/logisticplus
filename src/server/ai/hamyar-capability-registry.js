function field({
  aliases = [],
  source = "",
  sourceTool = "",
  safe = FIELD_POLICY.SAFE,
  freshness = FRESHNESS.MEMORY_CANDIDATE,
  memoryPolicy = MEMORY_POLICY.SUMMARY,
  answerTemplate = "",
  missingTemplate = "",
  notes = "",
} = {}) {
  return {
    aliases,
    source,
    sourceTool,
    safe,
    freshness,
    memoryPolicy,
    answerTemplate,
    missingTemplate,
    notes,
  };
}

